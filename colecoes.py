# Lista com 5 valores numéricos
numeros = [10, 5, 23, 7, 15]

# Exibir o maior e menor valor
print("Maior valor:", max(numeros))
print("Menor valor:", min(numeros))

# Ordenar a lista
numeros_ordenados = sorted(numeros)
print("Lista ordenada:", numeros_ordenados)

# Mostrar a quantidade de elementos
print("Quantidade de elementos:", len(numeros))

# Calcular a média dos valores
media = sum(numeros) / len(numeros)
print("Média dos valores:", media)